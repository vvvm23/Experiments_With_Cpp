#include <iostream>

int main() {
    std::cout << "Size of datatype int is: " << sizeof(int) << "\n";
    std::cout << "Size of datatype char is: " << sizeof(char) << "\n";
    std::cout << "Size of datatype float is: " << sizeof(float) << "\n";
    std::cout << "Size of datatype double is: " << sizeof(double) << "\n";
    return 0;
}